

string="asfhkTTYUfdERhjgWCBlkMJhggGDftyUERGgkp"

upper_count=0
lower_count=0

for ch in string:

    if ch.isupper():
        upper_count+=1

    elif ch.islower():
        lower_count+=1

print(f"upper count={upper_count}")
print(f"lower count={lower_count}")



       




    

       

       
